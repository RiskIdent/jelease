package main

import (
	"encoding/json"
	"errors"
	"fmt"
	"io"
	"log"
	"net/http"
	"os"

	jira "github.com/andygrunwald/go-jira"
	"github.com/joho/godotenv"
	"github.com/kelseyhightower/envconfig"
)

var (
	jiraClient *jira.Client
	config     Config
)

// Config contains configuration values from environment and .env file.
// Environment takes precedence over the .env file in case of conflicts.
type Config struct {
	Port          string   `envconfig:"PORT" default:"8080"`
	JiraUrl       string   `envconfig:"JIRA_URL" required:"true"`
	JiraUser      string   `envconfig:"JIRA_USER" required:"true"`
	JiraToken     string   `envconfig:"JIRA_TOKEN" required:"true"`
	Project       string   `envconfig:"PROJECT" required:"true"`
	DefaultStatus string   `envconfig:"DEFAULT_STATUS" required:"true"`
	AddLabels     []string `envconfig:"ADD_LABELS"`
}

// Release object unmarshaled from the newreleases.io webhook.
// Some fields omitted for simplicity, check out the documentation at https://newreleases.io/webhooks
type Release struct {
	Provider string `json:"provider"`
	Project  string `json:"project"`
	Version  string `json:"version"`
}

// Generates a Textual summary for the release, intended to be used as the Jira issue summary
func (release Release) IssueSummary() string {
	return fmt.Sprintf("Update %v to version %v", release.Project, release.Version)
}

func (release Release) JiraIssue() jira.Issue {
	labels := append(config.AddLabels, release.Project)
	return jira.Issue{
		Fields: &jira.IssueFields{
			Description: "Update issue generated by https://github.2rioffice.com/platform/jelease using newreleases.io.",
			Project: jira.Project{
				Key: config.Project,
			},
			Type: jira.IssueType{
				Name: "Task",
			},
			Status: &jira.Status{
				Name: config.DefaultStatus,
			},
			Labels:  labels,
			Summary: release.IssueSummary(),
		},
	}
}

// handleGetRoot handles to GET requests for a basic reachability check
func handleGetRoot(w http.ResponseWriter, r *http.Request) {
	fmt.Printf("health check request\n")
	io.WriteString(w, "Ok")
}

// handlePostWebhook handles newreleases.io webhook post requests
func handlePostWebhook(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, http.StatusText(http.StatusMethodNotAllowed), http.StatusMethodNotAllowed)
		fmt.Fprintf(os.Stderr, "Rejected request because: %v %v. Attempted method: %v",
			http.StatusMethodNotAllowed, http.StatusText(http.StatusMethodNotAllowed), r.Method)
		return
	}
	// parse newreleases.io webhook
	decoder := json.NewDecoder(r.Body)
	var release Release
	err := decoder.Decode(&release)
	if err != nil {
		http.Error(w, http.StatusText(http.StatusBadRequest), http.StatusBadRequest)
		fmt.Fprintf(os.Stderr, "Couldn't decode request body to json: %v\n error: %v\n", r.Body, err)
		return
	}
	fmt.Println(release)

	// look for existing update tickets
	previousIssuesQuery := fmt.Sprintf("status = %q and labels = %q", config.DefaultStatus, release.Project)
	previousIssues, resp, err := jiraClient.Issue.Search(previousIssuesQuery, &jira.SearchOptions{})
	if err != nil {
		fmt.Println(err.Error())
		body, readErr := io.ReadAll(resp.Body)
		if readErr != nil {
			panic(readErr)
		}
		fmt.Printf("Error response from Jira when searching previous issues: %+v\n", string(body))
		http.Error(w, http.StatusText(http.StatusBadGateway), http.StatusBadGateway)
		return
	}

	if len(previousIssues) == 0 {
		// no previous issues, create new jira issue
		i := release.JiraIssue()
		newIssue, response, err := jiraClient.Issue.Create(&i)
		if err != nil {
			fmt.Println(err.Error())
			body, readErr := io.ReadAll(response.Body)
			if readErr != nil {
				panic(readErr)
			}
			fmt.Printf("Error response from Jira when creating issue: %+v\n", string(body))
			http.Error(w, http.StatusText(http.StatusBadGateway), http.StatusBadGateway)
		}
		fmt.Printf("Created issue %v\n", newIssue.ID)
		return
	}

	// This seems hacky, but is taken from the official examples
	// https://github.com/andygrunwald/go-jira/blob/47d27a76e84da43f6e27e1cd0f930e6763dc79d7/examples/addlabel/main.go
	// There is also a jiraClient.Issue.Update() method, but it panics and does not provide a usage example
	type summaryUpdate struct {
		Set string `json:"set" structs:"set"`
	}
	type issueUpdate struct {
		Summary []summaryUpdate `json:"summary" structs:"summary"`
	}

	for _, previousIssue := range previousIssues {
		previousSummary := previousIssue.Fields.Summary

		updates := map[string]any{
			"update": issueUpdate{
				Summary: []summaryUpdate{
					{Set: release.IssueSummary()},
				},
			},
		}

		resp, err = jiraClient.Issue.UpdateIssue(previousIssue.ID, updates)
		if err != nil {
			fmt.Println(err.Error())
			body, readErr := io.ReadAll(resp.Body)
			if readErr != nil {
				panic(readErr)
			}
			fmt.Printf("Error response from Jira when updating issue: %+v\n", string(body))
			http.Error(w, http.StatusText(http.StatusInternalServerError), http.StatusInternalServerError)
		}

		fmt.Printf("Updated issue summary from %q to %q", previousSummary, release.IssueSummary())
	}
}

func main() {
	// configuration setup
	err := godotenv.Load()
	if err != nil {
		fmt.Println("No .env file found.")
	}

	err = envconfig.Process("jelease", &config)
	if err != nil {
		log.Fatal(err.Error())
	}

	fmt.Printf("Jira URL: %v\n", config.JiraUrl)
	tp := jira.BasicAuthTransport{
		Username: config.JiraUser,
		Password: config.JiraToken,
	}
	jiraClient, err = jira.NewClient(tp.Client(), config.JiraUrl)
	if err != nil {
		panic(err)
	}

	// Ensure config.Project exists on the Jira server
	allProjects, response, err := jiraClient.Project.GetList()
	if err != nil {
		fmt.Println(err.Error())
		body, decodingErr := io.ReadAll(response.Body)
		if err != nil {
			panic(decodingErr.Error())
		}
		fmt.Printf("Error response from Jira when retrieving project list: %+v\n", string(body))
		os.Exit(1)
	}
	var projectExists bool
	for _, project := range *allProjects {
		if project.Key == config.Project {
			projectExists = true
			break
		}
	}
	if !projectExists {
		fmt.Printf("Project %v does not exist on your Jira server", config.Project)
		os.Exit(1)
	}

	// Ensure config.DefaultStatus exists on the Jira server
	allStatuses, response, err := jiraClient.Status.GetAllStatuses()
	if err != nil {
		fmt.Println(err.Error())
		body, decodingErr := io.ReadAll(response.Body)
		if err != nil {
			panic(decodingErr.Error())
		}
		fmt.Printf("Error response from Jira when retrieving status list: %+v\n", string(body))
		os.Exit(1)
	}
	var statusExists bool
	for _, status := range allStatuses {
		if status.Name == config.DefaultStatus {
			statusExists = true
			break
		}
	}
	if !statusExists {
		fmt.Printf("Status %v does not exist on your Jira server", config.DefaultStatus)
		os.Exit(1)
	}

	// http server setup
	http.HandleFunc("/webhook", handlePostWebhook)
	http.HandleFunc("/", handleGetRoot)
	fmt.Printf("Listening on port %v\n", config.Port)
	err = http.ListenAndServe(fmt.Sprintf(":%v", config.Port), nil)
	if errors.Is(err, http.ErrServerClosed) {
		fmt.Printf("server closed\n")
	} else if err != nil {
		fmt.Printf("error starting server: %s\n", err)
		os.Exit(1)
	}
}
